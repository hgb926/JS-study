
var t = true, f = false;

// and연산 : 논리합 - &&

console.log(t && t);
console.log(t && f);
console.log(f && t);
console.log(f && f);

// or연산 : 논리곱 - ||

console.log('==================');
console.log(t || t);
console.log(t || f);
console.log(f || t);
console.log(f || f);

// not연산 : 논리반전 - !

console.log('==================');
console.log(!t);
console.log(!f);

